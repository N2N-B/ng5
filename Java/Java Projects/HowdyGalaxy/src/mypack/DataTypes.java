package mypack;

public class DataTypes {
	public static void main(String[] args) {
		boolean bn = true;
		char c = 'N';
		double d = 100d;
		float f = 509f;
		long l = 633;
		int i = 50;
		short s = 44;
		byte b = 100;
		String str = "Howdy";
		System.out.println(bn + " " + c + " " + d + " " + f + " " + l + " " + i + " " + s + " " + b + " " + str + " ");
	}
}

