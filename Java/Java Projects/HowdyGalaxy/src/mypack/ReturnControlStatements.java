package mypack;

public class ReturnControlStatements {

    int display()  
   {  
       return 8;  
   }  
   public static void main(String[] args) {  
	   ReturnControlStatements rs =new ReturnControlStatements();  
   System.out.println(rs.display());  
}  

}

